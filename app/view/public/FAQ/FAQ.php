  <h1>Foire aux Questions (FAQ)</h1>

  <section>
    <h2 id="q1">Qu’est-ce que signifie “fleurs éco-friendly” ?</h2>
    <p id="a1" class="answer">
      Les fleurs éco-friendly sont cultivées sans pesticides nocifs, avec des méthodes respectueuses de l’environnement, favorisant la biodiversité et réduisant l’empreinte carbone.
    </p>

    <h2 id="q2">D’où proviennent vos fleurs ?</h2>
    <p id="a2" class="answer">
      Nous sélectionnons nos fleurs auprès de producteurs locaux et certifiés bio afin de garantir fraîcheur et respect de la nature.
    </p>

    <h2 id="q3">Comment puis-je entretenir mes fleurs pour qu’elles durent plus longtemps ?</h2>
    <p id="a3" class="answer">
      Nous fournissons des conseils personnalisés avec chaque bouquet, comme changer l’eau régulièrement, couper les tiges en biais, et éviter l’exposition directe au soleil.
    </p>

    <h2 id="q4">Livrez-vous dans toutes les régions ?</h2>
    <p id="a4" class="answer">
      Nous livrons actuellement dans la majorité des grandes villes, avec des options de livraison écologique. Consultez notre page <a href="/livraison">Livraison</a> pour plus de détails.
    </p>
  </section>