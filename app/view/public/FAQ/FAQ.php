   <div class="faq-container">
     <h1>Questions fréquemment posées</h1>

     <details class="faq-question">
       <summary>
         <h3>Comment puis-je passer une commande ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Pour passer une commande, il suffit de parcourir notre catalogue, d'ajouter les produits souhaités à votre panier, puis de procéder au paiement en suivant les étapes indiquées.</p>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Quels sont les délais de livraison ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Les délais de livraison varient selon votre localisation :</p>
         <ul>
           <li>Bruxelles : Moins de 2 heures après avoir passé commande</li>
           <li>En péripherie et alentours: 2 heures après avoir passé commande</li>
           <li>Plus loins : Moins de 4 heures après avoir passé commande</li>
         </ul>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Puis-je retourner un produit ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Oui, vous disposez de 2 jours pour retourner un produit non satisfaisant. Le produit doit être dans son état d'origine avec son emballage.</p>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Quels moyens de paiement acceptez-vous ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Nous acceptons les moyens de paiement suivants :</p>
         <ul>
           <li>Carte bancaire (Visa, Mastercard)</li>
           <li>PayPal</li>
           <li>Virement bancaire</li>
         </ul>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Comment fonctionne la création de bouquet personnalisé ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Vous pouvez sélectionner chaque fleur individuellement dans notre catalogue.
           Chaque fleur est vendue à l'unité, vous permettant de créer exactement le bouquet que vous souhaitez.
           Ajoutez simplement les fleurs de votre choix à votre panier et nous les assemblerons avec soin.</p>
       </div>
     </details>
     <details class="faq-question">
       <summary>
         <h3>Combien de fleurs dois-je commander pour un bouquet ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Cela dépend de la taille de bouquet souhaitée ! 
          Pour un petit bouquet : 5-7 fleurs, pour un bouquet moyen : 10-15 fleurs, et pour un grand bouquet : 20+ fleurs.</p>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Comment conserver mes fleurs le plus longtemps possible ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Coupez les tiges en biseau sous l'eau froide, placez-les dans un vase propre avec de l'eau fraîche. Changez l'eau tous les 2 jours et recoupez légèrement les tiges.
           Évitez l'exposition directe au soleil et aux sources de chaleur.</p>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Proposez-vous des emballages cadeaux ?</h3>
       </summary>
       <div class="faq-answer">
         <p>Oui ! Nous proposons plusieurs options d'emballage : papier kraft naturel, papier coloré, ou papier transparent avec ruban.
           Vous pouvez également ajouter une carte message personnalisée gratuite.</p>
       </div>
     </details>

     <details class="faq-question">
       <summary>
         <h3>Puis-je programmer une livraison à une date précise ?</h3>
       </summary>
       <div class="faq-answer">
         Oui, vous pouvez choisir votre date de livraison jusqu'à 7 jours à l'avance. C'est parfait pour les anniversaires, Saint-Valentin, ou tout événement spécial.
         Un créneau horaire approximatif vous sera communiqué.
       </div>
     </details>
